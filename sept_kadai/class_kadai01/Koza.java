package class_kadai01;

public class Koza {
	private String name;
	private long money;

	/**
	 * @return name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name セットする name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return money
	 */
	public long getMoney() {
		return money;
	}

	/**
	 * @param money セットする money
	 */
	public void setMoney(long money) {
		this.money = money;
	}

}

package seiza02;

public class Numbers {

	/** 星座コード */
	private String constellationCode;

	/** 星座ラッキーナンバー */
	private String constellaLuckyNumber;

	/**
	 * @return constellationNumber
	 */
	public String getConstellationCode() {
		return constellationCode;
	}

	/**
	 * @param constellationNumber セットする constellationNumber
	 */
	public void setConstellationNumber(String constellationCode) {
		this.constellationCode = constellationCode;
	}

	/**
	 * @return constellaLuckyNumber
	 */
	public String getConstellaLuckyNumber() {
		return constellaLuckyNumber;
	}

	/**
	 * @param constellaLuckyNumber セットする constellaLuckyNumber
	 */
	public void setConstellaLuckyNumber(String constellaLuckyNumber) {
		this.constellaLuckyNumber = constellaLuckyNumber;
	}

}